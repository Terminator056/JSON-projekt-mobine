function age(rok_budowy) {
  let calculatedAge = new Date().getFullYear() - rok_budowy;
  if (calculatedAge == 1) {
    return "Tylko ma 1 rok po budowie";
  } else if (calculatedAge == 0) {
    return "Budynek ma do roku jak został zbudowany";
  } else {
    return `${calculatedAge} lat budynkowi`;
  }
}

function foods(foods) {
  return `
<h4>Dostępne miasta hoteli</h4>
<ul class="foods-list">
${foods.map(food => `<li>${food}</li>`).join("")}
</ul>
`;
}

function petTemplate(pet) {
  return `
    <div class="animal">
    <img class="pet-photo" src="${pet.photo}">
    <h2 class="pet-name">${pet.name} <span class=panstwo>(${pet.panstwo})</span></h2>
    <p><strong>Ilość lat budynkowi:</strong> ${age(pet.rok_budowy)}</p>
    ${pet.miasto ? foods(pet.miasto) : ""}
    </div>
  `;
}

